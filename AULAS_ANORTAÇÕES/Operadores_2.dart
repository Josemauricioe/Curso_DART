main(){
  // Operadores de Atribuicão (binário/infix)

  double a = 5;
  a = a+3;
  a += 3;
  a -= 5;
  a *= 4;
  a /= 3;
  a %= 2;

  print(a);

  //Operadores Relacionais (binário/infix) -> O resultado sempre bool

  print(3 > 2); //MAIOR
  print(3 >= 3); //MAIOR OU IGUAL
  print(3 < 4); //MENOR
  print(3 <= 3); //MENOR OU IGUAL
  print(3 != 3); //DIFERENTE
  print(3 == 3); //IGUAL  

  print(5 & 3);
  
 }